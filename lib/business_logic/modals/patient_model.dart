class Patient {
  final String uid;
  final String name;
  final String personalNumber;
  final String gender;
  final String birthDate;
  final String nationality;
  final String phone;
  final String email;

  const Patient({
    required this.uid,
    required this.name,
    required this.personalNumber,
    required this.gender,
    required this.birthDate,
    required this.nationality,
    required this.phone,
    required this.email,
  });
}
